"use strict";

// NOTE: The for Loop
/* 
A for loop is a control structure that allows you to execute a block of code multiple times based on a condition. It is commonly used for iterating over arrays, objects, and performing repetitive tasks.
*/

for (let rep = 1; rep <= 10; rep++) {
    console.log(`Lifting weights, 🏋️ rep ${rep} `);
}

/* 
1️⃣ Initialization: Runs once before the loop starts (e.g., let i = 0;).
2️⃣ Condition: The loop runs as long as this condition is true.
3️⃣ Increment/Decrement: Updates the loop variable (i++ or i--).
*/

/* 
✔ for loop repeats code a fixed number of times.
✔ Use for loop to iterate over arrays.
✔ Use for...in to iterate over objects.
✔ Use break to stop a loop early.
✔ Use continue to skip an iteration.
✔ forEach() is a modern alternative for iterating over arrays.
*/
